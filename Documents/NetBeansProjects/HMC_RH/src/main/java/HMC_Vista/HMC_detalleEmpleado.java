    private void btnGuardarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEmpleadoActionPerformed

        try {
            // =========================
            // 1. DATOS GENERALES
            // =========================
            String nombre = txtNombreBucar.getText().trim();
            String apePat = txtApellidoPatBuscar.getText().trim();
            String apeMat = txtApellidoMarBuscar.getText().trim();
            String email = txtEmailBuscar.getText().trim();
            String tel = txtTelefonoBuscar.getText().trim();
            String fechaNac = txtNacimientoBuscar.getText().trim();
            String fechaCon = txtContratacionBuscar.getText().trim();

            // =========================
            // 2. VALIDACIÓN BÁSICA
            // =========================
            if (nombre.isEmpty() || apePat.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nombre y Apellido son obligatorios");
                return;
            }

            // =========================
            // 3. PARSEO SEGURO
            // =========================
            double salario = parseDoubleSeguro(txtSalarioBuscar.getText());

            if (salario < 0) {
                JOptionPane.showMessageDialog(this, "Salario inválido");
                return;
            }

            HMC_Conexion con = new HMC_Conexion();
            boolean exito = false;

            // =========================
            // 4. DEFINIR OPERACIÓN
            // =========================
            boolean esNuevo = txtIdBuscar.getText().trim().isEmpty();

            // =========================
            // 5. INSERT
            // =========================
            if (esNuevo) {

                System.out.println("Estas creando un usuario");

                int idDep = parseEnteroSeguro(txtDepBuscar.getText());
                int idPuesto = parseEnteroSeguro(txtPuestoBuscar.getText());

                if (idDep <= 0 || idPuesto <= 0) {
                    JOptionPane.showMessageDialog(this, "Departamento y Puesto deben ser válidos");
                    return;
                }

                exito = con.insertarEmpleado(
                        nombre, apePat, apeMat,
                        email, tel, fechaNac, fechaCon,
                        salario, idDep, idPuesto
                );

                if (exito) {
                    JOptionPane.showMessageDialog(this, "Empleado creado exitosamente");
                }

            } else {
                // =========================
                // 6. UPDATE
                // =========================

                System.out.println("Estas EDITANDO un usuario");

                int idExistente = parseEnteroSeguro(txtIdBuscar.getText());

                if (idExistente <= 0) {
                    JOptionPane.showMessageDialog(this, "ID inválido");
                    return;
                }

                exito = con.actualizarEmpleado(
                        idExistente,
                        nombre,
                        apePat,
                        apeMat,
                        tel,
                        salario
                );

                if (exito) {
                    JOptionPane.showMessageDialog(this, "Empleado actualizado exitosamente");
                    btnEditarEmpleado.setEnabled(false);
                }
            }

            // =========================
            // 7. LIMPIAR FORMULARIO
            // =========================
            if (exito) {
                resetearFormulario();
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    "Error al guardar: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );

            e.printStackTrace();
        }

    }//GEN-LAST:event_btnGuardarEmpleadoActionPerformed

    private int parseEnteroSeguro(String valor) {
        try {
            if (valor == null || valor.trim().isEmpty()) {
                return 0;
            }
            return Integer.parseInt(valor.trim());
        } catch (NumberFormatException e) {
            return -1;
        }
    }

    private double parseDoubleSeguro(String valor) {
        try {
            if (valor == null || valor.trim().isEmpty()) {
                return 0;
            }
            return Double.parseDouble(valor.trim());
        } catch (NumberFormatException e) {
            return -1;
        }
    }


