    private void btnBuscarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEmpleadoActionPerformed
        HMC_Conexion con = new HMC_Conexion();

        int iDEmpleadoBuscar = Integer.parseInt(txtIdBuscar.getText());

        String empleadoEncontrado = con.buscarEmpleadoporId(iDEmpleadoBuscar);

        if (empleadoEncontrado != null && !empleadoEncontrado.isEmpty()) {
            String empleadoEncontradoBD[] = empleadoEncontrado.split("!");

            // Función auxiliar interna para limpiar nulos
            // (Puedes ponerla fuera del método si prefieres)
            java.util.function.Function<String, String> limpiar = (str)
                    -> (str == null || str.equals("null")) ? "" : str;

            txtIdBuscar.setText(limpiar.apply(empleadoEncontradoBD[0]));
            txtNombreBucar.setText(limpiar.apply(empleadoEncontradoBD[1]));
            txtApellidoPatBuscar.setText(limpiar.apply(empleadoEncontradoBD[2]));
            txtApellidoMarBuscar.setText(limpiar.apply(empleadoEncontradoBD[3]));
            txtEmailBuscar.setText(limpiar.apply(empleadoEncontradoBD[4]));
            txtDepBuscar.setText(limpiar.apply(empleadoEncontradoBD[5]));
            txtPuestoBuscar.setText(limpiar.apply(empleadoEncontradoBD[6]));
            txtSalarioBuscar.setText(limpiar.apply(empleadoEncontradoBD[7]));

            // --- MANEJO DE FECHAS (Nacimiento es el índice 8, Contratación el 9) ---
            txtNacimientoBuscar.setText(formatearFecha(limpiar.apply(empleadoEncontradoBD[8])));
            txtContratacionBuscar.setText(formatearFecha(limpiar.apply(empleadoEncontradoBD[9])));

            txtActivoBuscar.setText(limpiar.apply(empleadoEncontradoBD[10]));
            txtTelefonoBuscar.setText(limpiar.apply(empleadoEncontradoBD[11]));
            btnEditarEmpleado.setEnabled(true);
            btnEliminarEmpleado.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Empleado no encontrado!.");
            txtIdBuscar.setText("");
            txtNombreBucar.setText("");
            txtApellidoPatBuscar.setText("");
            txtApellidoMarBuscar.setText("");
            txtEmailBuscar.setText("");
            txtDepBuscar.setText("");
            txtPuestoBuscar.setText("");
            txtSalarioBuscar.setText("");
            txtNacimientoBuscar.setText("");
            txtContratacionBuscar.setText("");
            txtActivoBuscar.setText("");
            txtTelefonoBuscar.setText("");
            txtIdBuscar.requestFocusInWindow();

        }
    }//GEN-LAST:event_btnBuscarEmpleadoActionPerformed

